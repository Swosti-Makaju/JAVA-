public class identifiers {
    public static void main(String[] args) {
         // identifiers
        int _myNum = 15; // starts with an underscore
        int $myNum = 15; // starts with a dollar sign
        int myNum2 = 15; // starts with a letter and contains a number
        System.out.println(_myNum + " " + $myNum + " " + myNum2);
        // int 2myNum = 15; // Invalid - starts with a number
        // int my Num = 15; // Invalid - contains a space
        // int my-Num = 15; // Invalid - contains a hyphen
        // int my@Num = 15; // Invalid - contains a special character
    }      
}
